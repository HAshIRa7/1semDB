CONSTRAINT fk_people
FOREIGN KEY (patient_id)
REFERENCES People(human_id)

ALTER TABLE Doctors ADD 
CONSTRAINT doc_fk
FOREIGN KEY (doctor_id)
REFERENCES People(human_id),
