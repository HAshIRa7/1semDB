CREATE TABLE Patients (
patient_id INTEGER NOT NULL, 
police_number VARCHAR(100),
blood_number SMALLINT  CONSTRAINT YT CHECK(blood_number <= 4 AND blood_number >= 0), 
rhesus_factor VARCHAR(1) CONSTRAINT CH_rhesus CHECK(rhesus_factor = '+' OR rhesus_factor = '-'), 
CONSTRAINT pk_patietns PRIMARY KEY(patient_id)
); 

CREATE TABLE Social_status (
social_status_id SMALLINT NOT NULL  IDENTITY(1,1), 
social_status_number SMALLINT NOT NULL, 
social_status_start DATETIME, 
social_status_end DATETIME,
patient_id INTEGER,
CONSTRAINT pk_social_status_id PRIMARY KEY(social_status_id)
);

CREATE TABLE  Social_status_name (
social_status_number SMALLINT NOT NULL  IDENTITY(1,1),
social_status_name  VARCHAR(100),
CONSTRAINT pk_ssn PRIMARY KEY(social_status_number)
); 

CREATE TABLE People (
human_id INTEGER NOT NULL IDENTITY(1,1),
first_name VARCHAR(50),
last_name VARCHAR(100),
birth_date DATETIME,
gender_name VARCHAR(1),
CONSTRAINT pk_people  PRIMARY KEY(human_id) 
); 

CREATE TABLE Passport (
passport_id INTEGER  NOT NULL IDENTITY(1,1), 
passport_number VARCHAR(30),
passport_series VARCHAR(30), 
passport_start_date DATETIME,
passport_end_date DATETIME,
human_id INTEGER,
CONSTRAINT pk_pass PRIMARY KEY(passport_id)
);

CREATE TABLE Doctors (
doctor_id INTEGER NOT NULL, 
hire_date DATETIME,
manager_id INTEGER,
CONSTRAINT pk_doc PRIMARY KEY(doctor_id)
);

CREATE TABLE time_of_work (
time_id INTEGER NOT NULL IDENTITY(1,1),
start_of_work TIME,
end_of_work TIME,
day_of_work VARCHAR (11),
doctor_id INTEGER NOT NULL,
CONSTRAINT pk_time PRIMARY KEY(time_id)
);

CREATE TABLE Profession(
doctor_id INTEGER NOT NULL,
profession_id SMALLINT  NOT NULL, 
experience_year SMALLINT,
 CONSTRAINT pk_doctor PRIMARY KEY(doctor_id, profession_id)
);

CREATE TABLE Specialization(
profession_id SMALLINT NOT NULL IDENTITY(1,1),
profession_name VARCHAR(50)
CONSTRAINT pk_spec  PRIMARY KEY(profession_id)
); 

CREATE TABLE Visit (
visit_id INTEGER NOT NULL IDENTITY(1,1),
visit_time DATETIME,
patient_id INTEGER,
doctor_id INTEGER,
redirection_id INTEGER
CONSTRAINT pk_visit PRIMARY KEY(visit_id)
);

CREATE TABLE diagnosis (
diagnosis_id INTEGER NOT NULL,
diagnosis_name  VARCHAR(100),
visit_id INTEGER
CONSTRAINT pk_diag PRIMARY KEY(diagnosis_id)
);

CREATE TABLE Status_act(
status_number SMALLINT NOT NULL,
actual_status_name VARCHAR(50),
CONSTRAINT pk_status_number  PRIMARY KEY(status_number)
);

CREATE TABLE List_status(
status_id SMALLINT NOT NULL IDENTITY(1,1),
actual_status_number SMALLINT,
actual_status_starts DATETIME,
visit_id INTEGER
CONSTRAINT pk_ls PRIMARY KEY(status_id)
);

